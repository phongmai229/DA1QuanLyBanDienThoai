    private void btnCapnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapnhatActionPerformed
        // TODO add your handling code here:

        int check = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn cập nhật ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (check == JOptionPane.YES_OPTION) {
            String soLuongStr = txtSoluong.getText().trim().trim();
            String thongSoKTStr = txtThognsokithuat.getText().trim().trim();
            
            if (soLuongStr.equals("") || thongSoKTStr.equals("")) {
                JOptionPane.showMessageDialog(this, "Số lượng và thông số kỹ thuật không được bỏ trống");
            } else {
                try {
                    int soLuong = Integer.parseInt(soLuongStr);
                    int thongSoKT = Integer.parseInt(thongSoKTStr);
                    
                    if (soLuong <= 0 || soLuong >= 31 || thongSoKT <= 0 || thongSoKT >= 31) {
                        JOptionPane.showMessageDialog(this, "Số lượng và thông số kỹ thuật phải nằm trong khoảng từ 1 đến 30");
                    } else {
                        int row = tblCameraSau.getSelectedRow();
                        if (row == -1) {
                            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng trong bảng để cập nhật");
                        } else {
                            CameraService.sua(getForm(), list.get(row).getId());
                            list = CameraService.getAllCameraSauTheoTuMoiDenCu(0);
                            showData(list);
                            txtSoluong.setText("");
                            txtThognsokithuat.setText("");
                            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                        }
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Số lượng và thông số kỹ thuật phải là số nguyên");
                }
            }
        }

    }//GEN-LAST:event_btnCapnhatActionPerformed

