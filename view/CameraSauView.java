    private void cbbTrangthaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbTrangthaiActionPerformed
        // TODO add your handling code here:
        int a = cbbTrangthai.getSelectedIndex();
        list = CameraService.getAllCameraSauTheoTuMoiDenCu(a);
        showData(list);        
        if (a == 0) {            
            list = CameraService.getAllCameraSauTheoTuMoiDenCu(0);            
            btnKhoiphuc.setEnabled(false);
            btnCapnhat.setEnabled(true);
            btnXoa.setEnabled(true);
            showData(list);            
            
        } else if (a == 1) {
            list = CameraService.getAllCameraSauTheoTuMoiDenCu(1);
            btnCapnhat.setEnabled(false);
            btnXoa.setEnabled(false);
            btnKhoiphuc.setEnabled(true);
            showData(list);
        }
    }//GEN-LAST:event_cbbTrangthaiActionPerformed

