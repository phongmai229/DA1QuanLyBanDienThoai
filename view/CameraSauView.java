    private void showData(List<Camera> lists) {
        tableModel.setRowCount(0);
        for (Camera bn : lists) {
            tableModel.addRow(new Object[]{bn.getId(), bn.getSoluong(), bn.getThongsokithuat() + "MP", bn.getTrangthai() == 1 ? "Không hoạt động" : "Đang hoạt đông"});
        }
    }

